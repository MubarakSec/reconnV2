#!/bin/bash
# ╔═══════════════════════════════════════════════════════════╗
# ║              ReconnV2 - Cheatsheet                        ║
# ╚═══════════════════════════════════════════════════════════╝

cat << 'EOF'

╔═══════════════════════════════════════════════════════════════════════╗
║                    ReconnV2 Quick Reference                           ║
╚═══════════════════════════════════════════════════════════════════════╝

┌─────────────────────────────────────────────────────────────────────┐
│  🚀 الأوامر الأساسية                                                  │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  # فحص سريع                                                         │
│  recon-cli scan target.com --inline                                 │
│                                                                     │
│  # فحص سلبي (آمن)                                                   │
│  recon-cli scan target.com --profile passive --inline               │
│                                                                     │
│  # فحص شامل                                                         │
│  recon-cli scan target.com --profile full --inline                  │
│                                                                     │
│  # فحص عميق مع nuclei                                               │
│  recon-cli scan target.com --profile deep --scanner nuclei --inline │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────┐
│  📋 الملفات الشخصية (Profiles)                                       │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  passive    → فحص سلبي، آمن، لا يُكتشف                              │
│  full       → فحص نشط وشامل                                         │
│  quick      → فحص سريع، أساسي                                       │
│  deep       → فحص عميق، مكثف، طويل                                  │
│  bugbounty  → مُحسَّن لصيد الثغرات                                   │
│  stealth    → فحص خفي، بطيء، لتجنب الاكتشاف                         │
│  api-only   → APIs و GraphQL فقط                                    │
│  wordpress  → مُحسَّن لـ WordPress                                   │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────┐
│  🔧 الماسحات (Scanners)                                              │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  --scanner nuclei    → فحص ثغرات شامل                               │
│  --scanner wpscan    → فحص WordPress                                │
│                                                                     │
│  # مثال: استخدام الماسحين معاً                                       │
│  recon-cli scan wp.target.com --scanner nuclei --scanner wpscan     │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────┐
│  🎯 الوحدات النشطة (Active Modules)                                  │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  --active-module js-secrets  → اكتشاف الأسرار في JS                 │
│  --active-module backup      → اكتشاف ملفات النسخ الاحتياطي          │
│  --active-module cors        → فحص CORS                             │
│  --active-module diff        → مقارنة الاستجابات                     │
│                                                                     │
│  # مثال                                                              │
│  recon-cli scan target.com --active-module js-secrets               │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────┐
│  📁 أهداف متعددة                                                     │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  # إنشاء ملف أهداف                                                   │
│  echo -e "target1.com\ntarget2.com\ntarget3.com" > targets.txt      │
│                                                                     │
│  # فحص قائمة                                                         │
│  recon-cli scan --targets-file targets.txt --inline                 │
│                                                                     │
│  # فحص كل هدف في مهمة منفصلة                                        │
│  recon-cli scan --targets-file targets.txt --split-targets          │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────┐
│  📊 إدارة المهام                                                      │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  recon-cli list-jobs                  → عرض كل المهام               │
│  recon-cli list-jobs --status running → المهام الجارية              │
│  recon-cli status <job-id>            → حالة مهمة                   │
│  recon-cli tail-logs <job-id>         → متابعة السجلات              │
│  recon-cli requeue <job-id>           → إعادة تشغيل                 │
│  recon-cli export <job-id>            → تصدير النتائج               │
│  recon-cli report <job-id>            → إنشاء تقرير                 │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────┐
│  🐛 أوامر Bug Bounty الشائعة                                         │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  # فحص سريع لاكتشاف الأسرار                                          │
│  recon-cli scan target.com --active-module js-secrets --inline      │
│                                                                     │
│  # فحص شامل للثغرات                                                  │
│  recon-cli scan target.com --profile bugbounty \                    │
│      --scanner nuclei \                                             │
│      --active-module js-secrets \                                   │
│      --active-module backup --inline                                │
│                                                                     │
│  # فحص APIs                                                          │
│  recon-cli scan api.target.com --profile api-only --inline          │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────┐
│  💡 نصائح                                                            │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  • استخدم --force لإعادة تشغيل كل المراحل                           │
│  • استخدم -v أو -vv لزيادة التفاصيل                                  │
│  • النتائج في: jobs/finished/<job-id>/                              │
│  • الملفات المهمة: results.jsonl, results.txt                       │
│  • استخدم doctor لفحص النظام قبل البدء                              │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

┌─────────────────────────────────────────────────────────────────────┐
│  🔥 One-Liners                                                       │
├─────────────────────────────────────────────────────────────────────┤
│                                                                     │
│  # اكتشاف سريع للنطاقات الفرعية                                      │
│  recon-cli scan target.com -p passive --inline | grep hostname      │
│                                                                     │
│  # فحص سريع مع حفظ النتائج                                          │
│  recon-cli scan target.com -p quick --inline && \                   │
│      cat jobs/finished/*/results.txt                                │
│                                                                     │
│  # عرض آخر نتائج                                                     │
│  ls -t jobs/finished/ | head -1 | xargs -I {} \                     │
│      cat jobs/finished/{}/results.txt                               │
│                                                                     │
└─────────────────────────────────────────────────────────────────────┘

⚠️  تذكير: استخدم هذه الأداة فقط على الأنظمة المصرح لك باختبارها

EOF
